class TestManifestEmpty:
    def test_generate_empty_store(self, generator):
        """generate() returns valid markdown with no stage sections when store is empty."""
        result = generator.generate()
        assert result.startswith('# Memory')
        assert '<!-- Auto-generated by memesis plugin -->' in result
        # No stage headers when no memories
        assert '## Instinctive' not in result
        assert '## Crystallized' not in result
        assert '## Consolidated' not in result

    def test_token_budget_empty_store(self, generator):
        """estimate_token_budget() returns (0, 0.0) for empty store."""
        count, pct = generator.estimate_token_budget()
        assert count == 0
        assert pct == 0.0

    def test_write_manifest_creates_file(self, generator, store):
        """write_manifest() creates MEMORY.md in base_dir."""
        generator.write_manifest()
        manifest_path = store.base_dir / 'MEMORY.md'
        assert manifest_path.exists()
        content = manifest_path.read_text()
        assert '# Memory' in content

    def test_write_manifest_is_atomic(self, generator, store, monkeypatch):
        """write_manifest() uses tmp+rename — original file not corrupted on failure."""
        import shutil
        manifest_path = store.base_dir / 'MEMORY.md'

        # Write an initial valid manifest
        generator.write_manifest()
        original_content = manifest_path.read_text()
        assert '# Memory' in original_content

        # Patch shutil.move to fail mid-write
        def failing_move(src, dst):
            raise OSError("simulated disk full")
        monkeypatch.setattr(shutil, 'move', failing_move)

        # write_manifest() should raise but leave the original untouched
        with pytest.raises(OSError):
            generator.write_manifest()

        # Original file must be intact
        assert manifest_path.exists()
        assert manifest_path.read_text() == original_content


