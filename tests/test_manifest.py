class TestTokenBudget:
    def test_budget_reflects_file_sizes(self, store, generator):
        """Token count is roughly char_count // 4."""
        content = 'X' * 800  # 800 chars → ~200 tokens
        store.create('style.md', content, {'stage': 'instinctive', 'title': 'Style', 'summary': ''})

        count, pct = generator.estimate_token_budget()
        # File also has frontmatter, so char count will be > 800
        # Just verify it's in the right ballpark (within 10%)
        file_path = store.base_dir / 'instinctive' / 'style.md'
        actual_chars = len(file_path.read_text())
        expected_tokens = actual_chars // 4
        assert abs(count - expected_tokens) <= expected_tokens * 0.10

    def test_budget_percentage_is_fraction_of_200k(self, store, generator):
        """Percentage is token_count / 200_000."""
        count, pct = generator.estimate_token_budget()
        if count > 0:
            assert abs(pct - count / 200_000) < 1e-9
        else:
            assert pct == 0.0

    def test_budget_top10_crystallized_only(self, store, generator):
        """Only top-10 crystallized memories by importance contribute to budget."""
        # Create 12 crystallized memories with known importance values
        for i in range(12):
            store.create(
                f'c{i}.md',
                'Z' * 400,
                {
                    'stage': 'crystallized',
                    'title': f'Crystal {i}',
                    'summary': '',
                    'importance': i / 12,
                },
            )

        # Estimate includes only top-10 by importance
        count, _ = generator.estimate_token_budget()
        assert count > 0
        # The two lowest importance entries should not inflate the count
        # (there's no way to assert the exact boundary without knowing frontmatter size,
        # but we can verify count is less than all-12 would produce)
        crystallized_all = store.list_by_stage('crystallized')
        total_chars_all = sum(
            len(Path(m['file_path']).read_text()) for m in crystallized_all
        )
        tokens_all = total_chars_all // 4
        assert count < tokens_all  # top-10 < all-12


