class TestManifestIdempotency:
    def test_generate_is_idempotent(self, store, generator):
        """Same store state always produces the same manifest (minus timestamp)."""
        add_memory(store, 'instinctive', 'r.md', 'Ruby', 'Ruby style')
        add_memory(store, 'crystallized', 'c.md', 'Crystal', 'Crystal hint', importance=0.7)
        add_memory(store, 'consolidated', 'o.md', 'Cons', 'Consolidated note')

        result1 = generator.generate()
        result2 = generator.generate()

        # Strip the timestamp line before comparing
        def strip_timestamp(text):
            return '\n'.join(
                line for line in text.splitlines()
                if 'Last updated:' not in line
            )

        assert strip_timestamp(result1) == strip_timestamp(result2)

    def test_write_then_generate_stable(self, store, generator):
        """write_manifest() followed by generate() produces the same content."""
        add_memory(store, 'instinctive', 'r.md', 'Ruby', 'Ruby style')
        generator.write_manifest()

        manifest_path = store.base_dir / 'MEMORY.md'
        written = manifest_path.read_text()
        regenerated = generator.generate()

        def strip_timestamp(text):
            return '\n'.join(
                line for line in text.splitlines()
                if 'Last updated:' not in line
            )

        assert strip_timestamp(written) == strip_timestamp(regenerated)


