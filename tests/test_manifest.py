class TestManifestFormat:
    def test_instinctive_section_present(self, store, generator):
        add_memory(store, 'instinctive', 'ruby_style.md', 'Ruby Style', 'Idiomatic Ruby tips')
        result = generator.generate()
        assert '## Instinctive (always loaded)' in result
        assert '[Ruby Style]' in result
        assert 'Idiomatic Ruby tips' in result

    def test_crystallized_section_with_importance(self, store, generator):
        add_memory(store, 'crystallized', 'payment.md', 'Payment Pipeline', 'Check lock ordering', importance=0.78)
        result = generator.generate()
        assert '## Crystallized (context-matched)' in result
        assert '[importance: 0.78]' in result

    def test_consolidated_section_present(self, store, generator):
        add_memory(store, 'consolidated', 'arch.md', 'Memory Architecture', 'Chose lifecycle approach')
        result = generator.generate()
        assert '## Consolidated (available via search)' in result
        assert '[Memory Architecture]' in result

    def test_ephemeral_excluded(self, store, generator):
        add_memory(store, 'ephemeral', 'scratch.md', 'Scratch Note', 'Ephemeral content')
        result = generator.generate()
        # Ephemeral must not appear as a stage header or as an entry
        assert '## Ephemeral' not in result
        assert '[Scratch Note]' not in result

    def test_metadata_comment_includes_total_and_budget(self, store, generator):
        add_memory(store, 'instinctive', 'r.md', 'Ruby', 'Ruby style')
        add_memory(store, 'crystallized', 'c.md', 'Crystal', 'Crystal hint', importance=0.6)
        result = generator.generate()
        assert 'Total: 2' in result
        assert 'Token budget:' in result

    def test_relative_path_in_link(self, store, generator):
        """Link paths must be relative to store.base_dir."""
        add_memory(store, 'instinctive', 'style.md', 'Style Guide', 'Style notes')
        result = generator.generate()
        # Absolute path must not appear in the link
        assert str(store.base_dir) not in result
        assert 'instinctive/style.md' in result

    def test_summary_truncated_to_150_chars(self, store, generator):
        long_summary = 'A' * 200
        add_memory(store, 'consolidated', 'long.md', 'Long Entry', long_summary)
        result = generator.generate()
        # The displayed summary must be <= 150 chars + ellipsis
        # Find the line and check the dash-separated part
        for line in result.splitlines():
            if '[Long Entry]' in line:
                after_dash = line.split('\u2014', 1)[-1].strip()
                assert len(after_dash) <= 151  # 150 + ellipsis char
                assert after_dash.endswith('\u2026')
                break
        else:
            pytest.fail('Long Entry line not found in manifest')


