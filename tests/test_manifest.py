class TestWriteManifest:
    def test_writes_to_correct_location(self, store, generator):
        generator.write_manifest()
        expected = store.base_dir / 'MEMORY.md'
        assert expected.exists()

    def test_overwrites_existing_manifest(self, store, generator):
        """Calling write_manifest() twice overwrites the previous file."""
        add_memory(store, 'instinctive', 'r.md', 'Ruby', 'First summary')
        generator.write_manifest()

        first_content = (store.base_dir / 'MEMORY.md').read_text()

        # Update the memory title so the manifest differs
        memories = store.list_by_stage('instinctive')
        store.update(memories[0]['id'], metadata={'title': 'Updated Ruby'})
        generator.write_manifest()

        second_content = (store.base_dir / 'MEMORY.md').read_text()
        assert 'Updated Ruby' in second_content
