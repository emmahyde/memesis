class TestManifestOrdering:
    def test_crystallized_sorted_by_importance_descending(self, store, generator):
        add_memory(store, 'crystallized', 'low.md', 'Low', 'Low importance', importance=0.3)
        add_memory(store, 'crystallized', 'high.md', 'High', 'High importance', importance=0.9)
        add_memory(store, 'crystallized', 'mid.md', 'Mid', 'Mid importance', importance=0.6)

        result = generator.generate()
        high_pos = result.index('[High]')
        mid_pos = result.index('[Mid]')
        low_pos = result.index('[Low]')
        assert high_pos < mid_pos < low_pos

    def test_consolidated_sorted_by_updated_at_descending(self, store, generator):
        """Consolidated memories appear newest-first (updated_at DESC)."""
        # Create in order; the last one has the latest updated_at.
        add_memory(store, 'consolidated', 'first.md', 'First', 'Created first')
        add_memory(store, 'consolidated', 'second.md', 'Second', 'Created second')
        add_memory(store, 'consolidated', 'third.md', 'Third', 'Created third')

        result = generator.generate()
        # Third was created last → most recent updated_at → appears first
        third_pos = result.index('[Third]')
        second_pos = result.index('[Second]')
        first_pos = result.index('[First]')
        assert third_pos < second_pos < first_pos

    def test_stage_section_order(self, store, generator):
        """Instinctive section appears before Crystallized, which appears before Consolidated."""
        add_memory(store, 'instinctive', 'i.md', 'Inst', 'instinctive memory')
        add_memory(store, 'crystallized', 'c.md', 'Crys', 'crystallized memory', importance=0.7)
        add_memory(store, 'consolidated', 'o.md', 'Cons', 'consolidated memory')

        result = generator.generate()
        inst_pos = result.index('## Instinctive')
        crys_pos = result.index('## Crystallized')
        cons_pos = result.index('## Consolidated')
        assert inst_pos < crys_pos < cons_pos


